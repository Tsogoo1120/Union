export function getSchmishekLevel(total: number): {
  level: SchmishekLevel;
  label: string;
} {
  if (total <= 6) return { level: "low", label: "Бага" };
  if (total <= 12) return { level: "medium", label: "Дунд" };
  if (total <= 18) return { level: "above-medium", label: "Дундаас дээш" };
  return { level: "sharp", label: "Хурцдал" };
}

// Schmishek answers come from the generic TestRunner as
// { [questionIndex0Based]: optionIndex } where 0 = "Тийм", 1 = "Үгүй".
export function computeSchmishekScores(
  answers: Record<number, number>
): SchmishekScore[] {
  const isYes = (q1Based: number) => answers[q1Based - 1] === 0;
  const isNo = (q1Based: number) => answers[q1Based - 1] === 1;

  return SCHMISHEK_SCALES.map((scale) => {
    let raw = 0;
    for (const q of scale.yes) if (isYes(q)) raw += 1;
    for (const q of scale.no) if (isNo(q)) raw += 1;
    const total = raw * scale.multiplier;
    const { level, label } = getSchmishekLevel(total);
    return { scale, raw, total, level, levelLabel: label };
  });
}

export function getDominantSchmishekScales(
  scores: SchmishekScore[]
): SchmishekScore[] {
  return scores
    .filter((s) => s.level === "above-medium" || s.level === "sharp")
    .sort((a, b) => b.total - a.total);
}
